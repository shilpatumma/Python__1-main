# print weekday based on int inputs

weekday = int(input("\nEnter weekday number : "))

if (weekday == 1):    
    print("MONDAY\n")

elif (weekday == 2):
    print("TUESDAY\n")

elif (weekday == 3):
    print("WEDNESDAY\n")

elif (weekday == 4):
    print("THURSDAY\n")

elif (weekday == 5):
    print("FRIDAY\n")

elif (weekday == 6):
    print("SATURDAY\n")

elif (weekday == 7):
    print("SUNDAY\n")

else:
    print("Enter valid number...")     